    public static String getExcelTemplatePath() {
        String pathName = "";
        String os = System.getProperty("os.name");
        if (os != null && os.startsWith("Windows")) {
            pathName = "D:/excel_templates";
        } else {
            pathName = "/tmp/excel_templates";
        }
        return pathName;
    }

